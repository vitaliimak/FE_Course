function myFunction1() {
  // Get the checkbox
  var checkBox = document.getElementById('ch1');

  var firstItem = document.getElementById('firstname')

  //Did condition
  if (checkBox.checked == true) {
    firstItem.classList.replace('list-item', 'first');
  }
  else {
    firstItem.classList.replace('first', 'list-item');
  }
}

